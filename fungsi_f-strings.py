name = "joe"
age = 28

# String formatting tradisional
greeting = "Halo, nama saya " + name + " dan usia saya " + str(age)

# f-string formatting
f_greeting = f"Halo, nama saya {name} dan usia saya {age}"

print(greeting)  # Output: Halo, nama saya Reza dan usia saya 30
print(f_greeting)  # Output: Halo, nama saya Reza dan usia saya 30
